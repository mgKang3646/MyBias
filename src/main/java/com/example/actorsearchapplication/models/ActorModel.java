package com.example.actorsearchapplication.models;


public class ActorModel  {

    private String profile_path;
    private int id;
    private String name;
    private float popularity;


    public String getProfile_path() {
        return profile_path;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public float getPopularity() {
        return popularity;
    }
}
